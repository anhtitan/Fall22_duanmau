    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        if (Validate()) {
            if (cboVaiTro.getSelectedIndex() == 1) {
                String mk = new String(txtMK.getPassword());
                if (nhanvienSer.getOne(txtTaiKhoan.getText()) == null) {
                    JOptionPane.showMessageDialog(this, "TK khong ton tai");
                } else {
                    if (mk.equals(nhanvienSer.getOne(txtTaiKhoan.getText()).getMatKhau())) {
                        new view_deltais().setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "MK sai");
                    }
                }
            } else if (cboVaiTro.getSelectedIndex() == 2) {
                String mk = new String(txtMK.getPassword());
                if (khachhangSer.getOne(txtTaiKhoan.getText()) == null) {
                    JOptionPane.showMessageDialog(this, "tk k ton tai");
                } else {
                    if (mk.equals(khachhangSer.getOne(txtTaiKhoan.getText()).getMatKhau())) {
                        new view_KH().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "MK sai");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

