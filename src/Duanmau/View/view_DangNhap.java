    private Boolean Validate() {
        if (cboVaiTro.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn vai trò trước khi đăng nhập");
            return false;

        }
        if (txtTaiKhoan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "tài khoản không được để trống");
            return false;
        }
        if (txtMK.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "mật khấu không được để trống");
            return false;
        }

        return true;
    }

