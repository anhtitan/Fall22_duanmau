    private void loadDataSP(ArrayList<SanPham> list) {
        tblModelSP = (DefaultTableModel) tbl_DSSP.getModel();
        tblModelSP.setColumnIdentifiers(new String[]{"Mã SP", "Tên SP"});

        tblModelSP.setRowCount(0);
        for (SanPham x : list) {
            tblModelSP.addRow(new Object[]{x.getMa(), x.getTen()});
        }

    }

    private void loadTextSP() {
        int choice = tbl_DSSP.getSelectedRow();
        txtMaSP.setText(tbl_DSSP.getValueAt(choice, 0).toString());
        txtTenSP.setText(tbl_DSSP.getValueAt(choice, 1).toString());
    }

    private void Clear() {
        txtMaSP.setText("");
        txtTenSP.setText("");
    }

