    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        SanPham sp = new SanPham();
        sp.setMa(txtMaSP.getText());
        sp.setTen(txtTenSP.getText());
        if (SPSer.AddNew(sp)) {

            JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");

        } else {
            JOptionPane.showMessageDialog(this, "Thêm sản phẩm thất bại vui lòng kiểm tra lại");
        }
        loadDataSP(SPSer.getAll());
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String ma = txtMaSP.getText();
        SanPham sp = new SanPham();
        sp.setTen(txtTenSP.getText());
        if (SPSer.Edit(ma, sp)) {
            JOptionPane.showMessageDialog(this, "Update thành công");
            Clear();
        } else {
            JOptionPane.showConfirmDialog(this, "update thất bại vui lòng kiểm tra lại");
        }
        loadDataSP(SPSer.getAll());
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String ma = txtMaSP.getText();
        if (SPSer.Delete(ma)) {
            Clear();
            JOptionPane.showMessageDialog(this, "Xoá thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "xoa sản phẩm thất bại vui lòng kiểm tra lại");
        }
        loadDataSP(SPSer.getAll());
    }//GEN-LAST:event_btnDeleteActionPerformed

